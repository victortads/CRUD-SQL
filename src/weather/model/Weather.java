package weather.model;

public class Weather {

	public static int ID;
	public static float temperature;
	public static float lighting;
	public static float windSpeed;
	public static float humidity;
	
	public static int getID() {
		return ID;
	}
	public static void setID(int iD) {
		ID = iD;
	}
	public static float getTemperature() {
		return temperature;
	}
	public static void setTemperature(float temperature) {
		Weather.temperature = temperature;
	}
	public static float getLighting() {
		return lighting;
	}
	public static void setLighting(float lighting) {
		Weather.lighting = lighting;
	}
	public static float getWindSpeed() {
		return windSpeed;
	}
	public static void setWindSpeed(float windSpeed) {
		Weather.windSpeed = windSpeed;
	}
	public static float getHumidity() {
		return humidity;
	}
	public static void setHumidity(float humidity) {
		Weather.humidity = humidity;
	}
	
	
	
	
	  
}
